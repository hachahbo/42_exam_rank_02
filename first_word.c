#include <libc.h>

int main(int ac, char **av)
{
	int i;
	
	if(ac == 2)
	{
		i = 0;
		while(av[1][i])
		{
			if(av[1][i] == 9 || av[1][i] == 32)
				i++;
			else if(!(av[1][i] == 9 && av[1][i] == 32) && av[1][i])
			{
				write(1, &av[1][i], 1);
				i++;
			}
		}
		printf("\n");
	}
	else
		printf("\n");
	return(0);
}